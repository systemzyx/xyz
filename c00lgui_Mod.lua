-- Main highlighter
local function highlight(code)
	code = code:gsub("&", "&amp;"):gsub("<", "&lt;"):gsub(">", "&gt;"):gsub('"', "&quot;")
	local protected = {}
	local function protect(str)
	        table.insert(protected, str)
		return "\1PROTECT" .. #protected .. "\2"
	end

	-- Highlight multi-line strings: [[...]]
	code = code:gsub("%[%[.-%]%]", function(s)
		return protect('<font color="#ce9178">' .. s .. '</font>')
	end)

	-- Strings (single and double quoted)
	-- Match double-quoted strings with escaped quotes
        code = code:gsub('"(.-[^\\])?"', function(s)
	       return protect('<font color="#ce9178">'..s..'</font>')
        end)

       -- Match single-quoted strings with escaped quotes
        code = code:gsub("'(.-[^\\])?'", function(s)
	       return protect('<font color="#ce9178">'..s..'</font>')
        end)

	-- Highlight numbers
	code = code:gsub("(%d+%.?%d*e?%-?%d*)", function(s)
		return '<font color="#ff7d7d">' .. s .. '</font>'
	end)

	-- Highlight brackets: (), [], {}
	code = code:gsub("([%(%){%}%[%]])", function(b)
		local color = tokenColors[b]
		if color then
			return '<font color="' .. color .. '">' .. b .. '</font>'
		end
		return b
	end)

	-- Highlight keywords, builtins, etc.
	code = code:gsub("([%a_][%w_]*)", function(word)
		local color = tokenColors[word]
		if color then
			return '<font color="' .. color .. '">' .. word .. '</font>'
		end
		return word
	end)

	-- Restore protected strings
	code = code:gsub("\1PROTECT(%d+)\2", function(i)
		return protected[tonumber(i)]
	end)

	return code
end
local function update()
		local rawText = codeBox.Text
		highlighter.Text = highlight(rawText)
	
		-- Optional: auto-resize based on lines
		local lines = select(2, rawText:gsub("\n", "\n")) + 1
		local lineHeight = codeBox.TextSize + 4
		local newHeight = lines * lineHeight
		container.Size = UDim2.new(1, 0, 0, newHeight)
	end

codeBox:GetPropertyChangedSignal("Text"):Connect(update)

codeBox.Text = ""
update()
end
